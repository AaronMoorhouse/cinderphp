	<h2>Sample Text</h2>
	<p>The Lords of Cinder are all heroes who have linked the First Flame and prolonged the Age of Fire, rather than become Dark Lords.</p>
	<p>
		They are beings who have linked the Fire in a past age, restoring the power of flame and becoming powerful ashen lords. The original Lord of Cinder was Gwyn, who connected his soul to the flame, 
		and began the never-ending cycle of Disparity, Light, and Dark. within the world. The Chosen Undead from Anor Londo rose to become a Lord, and so did the Bearer of the Curse from Drangleic, a land 
		known for the Legend of the Linking of the Fire. The Lords, who have abandoned their thrones, used their souls of ash and cinder as kindling for the Fire, to re-enact the first linking.
	</p>
	<p>There are five Lords of Cinder:</p>
	<ul>
		<li>Abyss Watchers</li>
		<li>Aldrich, Devourer of Gods</li>
		<li>Lothric, Younger Prince</li>
		<li>Ludleth of Courland</li>
		<li>Yhorm the Giant</li>
	</ul>
	<h3>Where the Lords Come From</h3>
	<p>
		The Lords of Cinder are reborn when the Bells of Awakening are rung. Like in Dark Souls, you ring the bells and the gods appear, and from there you can push the cycle further from the Dark Age 
		by taking Gwyn's place, or you can refuse to link the fire. In both situations, the cycle is not ended and a new world emerges, as in Dark Souls II.
	</p>
	<p>
		In the intro cinematic, the narrator says this about the Unkindled: "Nameless accursed undead, unfit even to be cinder." Perhaps the Unkindled is the one that participated in killing the gods but 
		refused to link the fire. The Lords of Cinder are the ones that did the same but decided to sacrifice themselves to link the fire. As the flame is fading and the dark is still coming, someone rung 
		the Bells of Awakening in another time in hopes of breaking the cycle. So, every Lord of Cinder and Unkindled awake themselves one last time.
	</p>
